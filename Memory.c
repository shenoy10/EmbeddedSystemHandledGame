void MemoryEngine (void)
{
		stage=1;
		Random_Init(NVIC_ST_CURRENT_R);
		uint8_t mynum=RandomGeno();
	
		switch(mynum)
		{
				
			case 0:
				game=0;
				break;
			case 1:
				game=1;
				break;
			case 2:
				game=2;
				break;
			case 3:
				game=3;
				break;
		
		}
		
		
		MemoryGame_Run();
		
		
		
	
	
	
	
	



}

void MemoryGame_Run(void)
{
		do
		{
		ProcessStage(stage);
		DrawBig(bigs[game].answers[stage-1]);
		while((GPIO_PORTE_DATA_R&0xF)==0){}
		if((GPIO_PORTE_DATA_R&0xF)==games[game].answers[stage-1])
		{
				stage++;
				ST7735_FillRect(0,35,128,160,0);
				ST7735_SetCursor(5,5);
				ST7735_OutString("Correct!");
				ST7735_SetCursor(1,6);
				ST7735_OutString("Onto the next Stage");
				Send(0xB000);
				for(int i=0; i<10000000; i++){}
		
		}
		else 
		{
			AddStrike();
			for(int i=0; i<1000000; i++){}
		}
	
		}while(stage<5);
	
		ST7735_SetCursor(7,8);
		ST7735_OutString("JK!");



}
